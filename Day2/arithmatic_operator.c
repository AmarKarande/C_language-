#include<stdio.h>
int main(){
    // Arithmatic operator
    /**
     * +
     * -
     * *
     * /
     * %
     * ++
     * --
     */
    int a=30;
    int b=20;
    int c=10;
    // Additon
    printf("a+b = %d \n",a+b);
    // Subtraction
    printf("a-b = %d \n",a-b);
    // Multiplication
    printf("a*c = %d \n",a*c);
    // Division
    printf("a/c = %d \n",a/c);
    // Modulus
    printf("a%b = %d \n",a%b);
    // Increment
    printf("a=== = %d \n",a);
    a++;
     printf("a++ = %d \n",a);
    
    // Decrement
    printf("b-- === %d \n ",b);
    b--;
    printf("b-- == %d \n",b);
    return 0;
}